package fundamentos;

public class Conversaonumerica {
	public static void main(String[] args) {
		// Exemplo 1- Conversão explícta
		float f = (float) 0.1;
		System.out.println(f);
		// Exemplo 2- Conversão explícta
		int i1 = (int) 7.9;
		System.out.println(i1);
		// Exemplo 3- Conversão implícta
		int i2 = 'a';
		System.out.println(i2);
		// Exemplo 4- Conversão implícta
		double d = 1001;
		System.out.println(d);

	}
}
